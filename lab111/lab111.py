#### listas tuplas y diccionarios
#### definición de una lista
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

#### acceso a una lista por posición
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])
myFruitList[2] = "orange"
print(myFruitList)

##### tipo de datos tupla
myFinalAnswerTuple = ("apple", "banana", "pineapple")
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))
print(str(myFinalAnswerTuple) + " is of the data type " + str(type(myFinalAnswerTuple)) + ".")

#### acceso a una tupla por posición
print(myFinalAnswerTuple[0])

print(myFinalAnswerTuple[1])

print(myFinalAnswerTuple[2])

print(myFinalAnswerTuple[0])

#### tipo de dato diccionario

myFavoriteFruitDictionary = {
    "alicia" : "apple",
    "doriane" : "banana",
    "oscar" : "tomato"
}
print(myFavoriteFruitDictionary)

print(type(myFavoriteFruitDictionary))

#### acceso al diccionario por nombre

print(myFavoriteFruitDictionary["alicia"])
print(myFavoriteFruitDictionary["doriane"])
print(myFavoriteFruitDictionary["oscar"])

